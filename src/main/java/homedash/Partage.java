public class Partage {
    private String file_name;
    private String proprietaire;
    private String date_partage;
    private String file_size;
    private String permissions;

    public Partage(String file_name, String proprietaire, String date_partage, String file_size, String permissions) {
        this.file_name = file_name;
        this.proprietaire = proprietaire;
        this.date_partage = date_partage;
        this.file_size = file_size;
        this.permissions = permissions;
    }

    // Getters et setters
    public String getFile_name() { return file_name; }
    public void setFile_name(String file_name) { this.file_name = file_name; }

    public String getProprietaire() { return proprietaire; }
    public void setProprietaire(String proprietaire) { this.proprietaire = proprietaire; }

    public String getDate_partage() { return date_partage; }
    public void setDate_partage(String date_partage) { this.date_partage = date_partage; }

    public String getFile_size() { return file_size; }
    public void setFile_size(String file_size) { this.file_size = file_size; }

    public String getPermissions() { return permissions; }
    public void setPermissions(String permissions) { this.permissions = permissions; }
}


